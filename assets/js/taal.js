function toggleLanguage() {
  currentLanguage = currentLanguage === "nl" ? "en" : "nl";
  updateLanguage();
  updateFlag();
}

function updateLanguage() {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (translations[currentLanguage][key]) {
      element.textContent = translations[currentLanguage][key];
    }
  });
  // Update document language attribute
  document.documentElement.lang = currentLanguage;
  // Save language preference
  localStorage.setItem("preferredLanguage", currentLanguage);
}

function updateFlag() {
  const flagElement = document.getElementById("languageFlag");
  if (currentLanguage === "nl") {
    flagElement.src = "assets/img/netherlands.png";
    flagElement.alt = "Switch to English";
  } else {
    flagElement.src = "assets/img/united-kingdom.png";
    flagElement.alt = "Switch to Dutch";
  }
}

function initializeLanguage() {
  // Check for saved language preference
  const savedLanguage = localStorage.getItem("preferredLanguage");
  if (savedLanguage && translations[savedLanguage]) {
    currentLanguage = savedLanguage;
  }
  updateLanguage();
  updateFlag();
}

document.addEventListener("DOMContentLoaded", () => {
  initializeLanguage();
  const flagElement = document.getElementById("languageFlag");
  if (flagElement) {
    flagElement.addEventListener("click", toggleLanguage);
  }
});
